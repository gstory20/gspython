/****************************************************************************
*****************************************************************************
*****************************************************************************

        NAME:           strupr

        Purpose:        To convert a string to upper case

        Description:    This function converts a string to upper case.

        Arguments:      1.  String

        Processing:     Loop through the string converting as you go

        Returns:        nothing

*****************************************************************************
*****************************************************************************
****************************************************************************/

void strupr (string)

char *string;

{
   char *my_string;

   for (my_string = string; *my_string != 0; ++my_string)
      *my_string = (char) chupper (*my_string);

}  /* strupr  */


/****************************************************************************
*****************************************************************************
*****************************************************************************

        NAME:           strlowr

        Purpose:        To convert a string to upper case

        Description:    This function converts a string to upper case.

        Arguments:      1.  String

        Processing:     Loop through the string converting as you go

        Returns:        nothing

*****************************************************************************
*****************************************************************************
****************************************************************************/

void strlwr (string)

char *string;

{
   char *my_string;

   for (my_string = string; *my_string != 0; ++my_string)
      *my_string = (char) chlower (*my_string);

}  /* strlowr  */


